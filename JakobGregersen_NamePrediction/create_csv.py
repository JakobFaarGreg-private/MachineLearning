def createHeaders():
    nameList = list()
    for i in range(290):
        nameList.append("Name1")
        
    for i in range(290):
        nameList.append("Name2")
        
    for j in range(len(nameList)):
        nameList[j] += "Letter" + str(j%10+1)
        
    for k in range(len(nameList)):
        nameList[k] += "Node" + str(k%29+1)
        
    return nameList
