def nameToBinaryNodes(str):
    stringList = str.upper()
    returnList = list()
    for i in range(len(stringList)):
        returnList.append(nodeDictionary[stringList[i]])
    
    while(len(returnList)!=10):
        returnList.append(nodeDictionary[""])
        
    returnList = [item for sublist in returnList for item in sublist]
    return returnList
